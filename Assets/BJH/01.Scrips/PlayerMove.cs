using UnityEngine;
using UnityEngine.AI;
using Photon.Pun;
using TMPro;


public class PlayerMove : MonoBehaviourPun
{
    [SerializeField]
    float speed;
    public float walkSpeed;
    public float runSpeed;
    public Transform player;
    public Transform trCam;

    public bool canMove = true;
    public bool isMoving = false;

    public Animator[] animator;

    public GameObject PlayerRig;
    CharacterController cc;

    // 중력
    float gravity = -9.8f;
    private Vector3 velocity;

    // 터치 이동
    Vector3 targetPosition;


    private void Start()
    {
        // 내 플레이어 일때만 카메라를 켠다.
        if (photonView.IsMine)
        {
            trCam.gameObject.SetActive(true);
        }

        cc = PlayerRig.GetComponent<CharacterController>();
    }

    private void Update()
    {
        // 내 플레이어 가 아니면 걷지 않는다.
        if (!photonView.IsMine)
        {
            return;
        }

        // 걸을 수 없는 상태라면 걷지 않는다.
        if (!canMove)
        {
            return;
        }

        if ((Input.touchCount > 0) )
        {
            Touch touch = Input.GetTouch(0);

            if(touch.phase == TouchPhase.Began)
            {
                targetPosition = Camera.main.ScreenToWorldPoint(touch.position);
                targetPosition.z = transform.position.z;
                isMoving = true;
            }
        }

        if(isMoving)
        {
            transform.position = Vector3.MoveTowards(transform.position, targetPosition, Time.deltaTime * speed);

            // 목표 위치에 도달하면 이동 중지
            if (transform.position == targetPosition)
            {
                isMoving = false;
            }
        }

#if PC
        // 키 입력 및 방향 설정
        float h = Input.GetAxisRaw("Horizontal");
        float v = Input.GetAxisRaw("Vertical");
        Vector3 dir = new Vector3(h, 0, v).normalized;

        isMoving = h != 0f || v != 0f;

        // 움직이지 않으면
        if(!isMoving)
        {
            speed = 0f;
        }
        // 움직이면
        if(isMoving)
        {
            // 걸을 때
            speed = 5f;

            // 뛸 때
            if(Input.GetKey(KeyCode.RightShift))
            {
                speed = 10f;
            }
        }

        // 이동
        //transform.position += dir * speed * Time.deltaTime;
        cc.Move(dir* speed *Time.deltaTime);

        // 애니메이션 적용(Photon X)
        for (int i = 0; i < animator.Length; i++) 
        {
            if(animator[i].gameObject.activeSelf == false)
            {
                continue;
            }
            animator[i].SetFloat("MoveSpeed", speed);
        }

        trCam.position = player.transform.position + new Vector3(0, 1.5f, -3f);
        trCam.rotation = Quaternion.Euler(22f, 0, 0);

        // 중력 적용
        velocity.y += gravity * Time.deltaTime;

        // 수직 이동
        cc.Move(velocity * Time.deltaTime);
    }
#endif
}
